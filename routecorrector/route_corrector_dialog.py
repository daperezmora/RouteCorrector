    # -----------------------------
    def update_points_table(self):
        """The table shows *only* anchors (what you moved)."""
        self.tableWidget.setRowCount(len(self.manual_points))
        for row, fid in enumerate(self.anchor_order):
            data = self.manual_points.get(fid, {})
